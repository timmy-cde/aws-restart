# List
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

myFruitList[2] = "orange"
print(myFruitList)

# Tuple
myFinalAnswerTuple = ("apple", "banana", "pinapple")
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))

print(myFinalAnswerTuple[0])
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2])

# Dictionary
myFavoriteDictionary = {
    "Akua": "apple",
    "Saanvi": "banana",
    "Paulo": "pineapple"
}
print(myFavoriteDictionary)
print(type(myFavoriteDictionary))

print(myFavoriteDictionary["Akua"])
print(myFavoriteDictionary["Saanvi"])
print(myFavoriteDictionary["Paulo"])